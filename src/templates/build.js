function fontLinks(skin, brand) {
  const params = [...skins[skin].fonts.params]
  const override = headlineFonts[brand.font]
  if (override?.param && !params.includes(override.param)) params.push(override.param)
  const href = `https://fonts.googleapis.com/css2?${params.map((p) => `family=${p}`).join('&')}&display=swap`
  return `<link rel="preconnect" href="https://fonts.googleapis.com">
  <link rel="preconnect" href="https://fonts.gstatic.com" crossorigin>
  <link href="${href}" rel="stylesheet">`
}

function shell({ title, description, body, skin, brand, inlineCss, previewScript }) {
  const css = inlineCss ? `<style>${inlineCss}</style>` : '<link rel="stylesheet" href="css/styles.css">'
  const scripts = inlineCss
    ? `<script>${mainJs}</script>${previewScript || ''}`
    : '<script src="js/main.js"></script>'
  return `<!doctype html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>${esc(title)}</title>
  <meta name="description" content="${esc(description)}">
  ${fontLinks(skin, brand)}
  ${css}
</head>
<body>
${body}
${scripts}
</body>
</html>`
}

// Give a section a background image: a .sec-bg layer with its own opacity sits
// under the content (which gets lifted above it via .has-bg in baseCss).
function injectBg(html, bg) {
  const m = html.match(/<(section|div)([^>]*)>/)
  if (!m || !bg?.image) return html
  const url = String(bg.image).replace(/["'\\\s]/g, '').replace(/&/g, '&amp;')
  const opacity = Math.min(1, Math.max(0.05, Number(bg.opacity) || 0.25))
  const rootClass = `has-bg${bg.invert ? ' bg-invert' : ''}`
  const attrs = m[2].includes('class="') ? m[2].replace('class="', `class="${rootClass} `) : `${m[2]} class="${rootClass}"`
  const layer = `<div class="sec-bg" style="background-image:url('${url}');opacity:${opacity}" aria-hidden="true"></div>`
  return html.replace(m[0], `<${m[1]}${attrs}>${layer}`)
}

// Render a page's visible sections in layout order. Each section's root
// element gets data-sec="<key>" so the preview can map clicks back to the
// matching editor group (stripped from exports).
function renderSections(layout, registry, ctx) {
  return layout
    .filter((s) => !s.hidden && registry[s.key])
    .map((s) => injectBg(registry[s.key].render(ctx).replace(/<(section|div)\b/, `<$1 data-sec="${s.key}"`), s.bg))
    .join('\n')
}

// Inline-editing annotations are a preview affordance only.
const stripEditAttrs = (html) => html.replace(/ data-(edit|edit-rich|edit-ml|sec|buy)="[^"]*"/g, '')

