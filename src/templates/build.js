function buildCss(config) {
  const { brand, skin } = config
  const skinDef = skins[skin]
  const override = headlineFonts[brand.font]
  const displayFamily = override?.family || skinDef.fonts.display
  return `/* Generated by Buildrr — edit freely. Brand colors live in :root below. */
${cssVars(brand, brand.tone)}
:root {
  --font-display: ${displayFamily};
  --font-body: ${skinDef.fonts.body};
  --display-weight: ${skinDef.fonts.displayWeight};
  --display-tracking: ${skinDef.fonts.tracking};
}
${baseCss}
${placeholderCss}
/* ---- Skin: ${skinDef.name} ---- */
${skinDef.css}`
}

// The "how your forms capture emails" section, specific to the chosen destination.
function captureReadme(config) {
  const capture = config.pageType === 'optin' ? config.optin.capture : config.pageType === 'site' ? config.site.capture : null
  if (!capture) return '' // sales page sells via checkout links, no email form
  const D = capture.destination
  const head = '## Your email capture\n'
  if (D === 'netlify') {
    return `${head}This site uses **Netlify Forms**. Deploy to Netlify (drag the folder onto
https://app.netlify.com/drop) and every submission shows up under Forms in your
Netlify dashboard — turn on email notifications there, or forward to your email tool
via Netlify's Zapier/Make integration. Capture only works on Netlify; on other hosts
the form still sends visitors to thank-you.html.
`
  }
  if (D === 'kit') {
    return `${head}Submissions go to your **ConvertKit / Kit** form at:
\`${capture.actionUrl || '(set the form URL in Buildrr)'}\`
Visitors stay on your own thank-you.html. To change it, edit the form's \`action\` in the HTML.
`
  }
  if (D === 'mailchimp') {
    return `${head}Submissions go to **Mailchimp** at:
\`${capture.actionUrl || '(set the form URL in Buildrr)'}\`
Mailchimp shows its own confirmation page after signup (that's how their embedded
forms work). Edit the form's \`action\` in the HTML to change it.
`
  }
  if (D === 'mailerlite') {
    return `${head}Submissions go to **MailerLite** at:
\`${capture.actionUrl || '(set the form URL in Buildrr)'}\`
MailerLite shows its own confirmation page after signup. Edit the form's \`action\` to change it.
`
  }
  if (D === 'formsubmit') {
    return `${head}Submissions are emailed to you via **FormSubmit** (${capture.email || 'your email'}).
The first time someone submits, FormSubmit sends you a one-time confirmation email —
click it once to activate. Visitors stay on your own thank-you.html.
`
  }
  if (D === 'custom') {
    return `${head}Submissions post to your endpoint:
\`${capture.actionUrl || '(set the URL in Buildrr)'}\`
${capture.customAjax ? 'Sent in the background; visitors stay on thank-you.html.' : 'A normal form POST; the response page is whatever your endpoint returns.'}
`
  }
  return `${head}No email tool is connected yet — the form just forwards visitors to
thank-you.html. Open the **Publish** tab in Buildrr to connect ConvertKit, Mailchimp,
MailerLite, FormSubmit, Netlify Forms, or a custom endpoint.
`
}

function readme(config, pages) {
  const { brand } = config
  return `# ${brand.name} — exported from Buildrr

Plain HTML/CSS/JS. No build step, no dependencies. Upload the whole folder anywhere
that serves static files and you're live.

## Files
${pages.map((p) => `- ${p.file} — ${p.title}`).join('\n')}
- css/styles.css — all styles (brand colors are CSS variables at the top)
- js/main.js — scroll reveals, mobile nav, form handling (a few KB, no libraries)

## Deploy in one minute
- **Netlify Drop**: drag this folder onto https://app.netlify.com/drop
- **Vercel**: run \`npx vercel\` in this folder
- **Cloudflare Pages**: drag the folder into a new Pages project
- **GitHub Pages**: push to a repo, enable Pages in settings
- **Any classic host (cPanel/FTP)**: upload everything to public_html

${captureReadme(config)}
## Change colors later
Open css/styles.css — the first :root block holds --brand and --accent. Everything
derives from those two colors.
`
}

export function buildFiles(config, { preview = false, previewScript = '' } = {}) {
  const { pageType, brand } = config
  const css = buildCss(config)
  const wrap = (title, description, body) =>
    shell({ title, description, body, skin: config.skin, brand, inlineCss: preview ? css : null, previewScript: preview ? previewScript : '' })

  let pages = []
  const files = {}

  if (pageType === 'optin') {
    const d = config.optin
    pages = [
      { file: 'index.html', title: 'Opt-in' },
      { file: 'thank-you.html', title: 'Thank you' },
    ]
    const body = `
${simpleNav(brand, d.hero.ctaLabel, '#signup')}
<main>
${renderSections(d._layout, registryFor('optin'), { d, brand })}
</main>
${footer(brand, d.settings.footerNote)}`
    files['index.html'] = wrap(`${brand.name} — ${d.hero.eyebrow}`, d.hero.subhead.slice(0, 155), body)
    files['thank-you.html'] = wrap(
      `You're in — ${brand.name}`,
      'Check your inbox.',
      thankYouBody(brand, 'You’re in. Check your inbox.', 'Your first lesson is on its way. If it’s not there in two minutes, peek at your spam folder and drag it to your inbox.', 'index.html')
    )
  } else if (pageType === 'sales') {
    const d = config.sales
    pages = [{ file: 'index.html', title: 'Sales page' }]
    // Buy buttons scroll to the offer box by default; "directCta" (or a
    // hidden/removed offer section) sends them straight to the checkout link.
    const offerVisible = d._layout.some((s) => s.key === 'offer' && !s.hidden)
    const checkout = extUrl(d.offer.checkoutUrl)
    const buyHref = d.settings.directCta && checkout ? checkout : offerVisible ? '#offer' : checkout || '#'
    const body = `
${simpleNav(brand, d.hero.ctaLabel, buyHref, null, ` data-buy="${checkout ? 'ok' : 'missing'}"`)}
<main>
${renderSections(d._layout, registryFor('sales'), { d, brand, buyHref })}
</main>
${footer(brand, d.settings.footerNote)}`
    files['index.html'] = wrap(`${brand.name} — ${d.hero.headline.replace(/\*/g, '')}`, d.hero.subhead.slice(0, 155), body)
  } else {
    const d = config.site
    pages = [...sitePages, { file: 'thank-you.html', title: 'Thank you' }]
    const pageMeta = {
      'index.html': { key: 'home', title: `${brand.name} — ${d.global.tagline}`, desc: d.home.hero.subhead },
      'about.html': { key: 'about', title: `About — ${brand.name}`, desc: d.about.hero.subhead },
      'services.html': { key: 'services', title: `Services — ${brand.name}`, desc: d.services.hero.subhead },
      'contact.html': { key: 'contact', title: `Contact — ${brand.name}`, desc: d.contact.hero.subhead },
    }
    for (const [file, meta] of Object.entries(pageMeta)) {
      const body = `
${siteNav(brand, sitePages, file, d.global.ctaLabel)}
<main>
${renderSections(d._layout[meta.key], registryFor('site', meta.key), { d, brand })}
</main>
${footer(brand, d.settings.footerNote)}`
      files[file] = wrap(meta.title, meta.desc.slice(0, 155), body)
    }
    files['thank-you.html'] = wrap(
      `Thanks — ${brand.name}`,
      'Message received.',
      thankYouBody(brand, 'Got it. Talk soon.', 'Your message is in our inbox and we reply to every note within one business day.', 'index.html')
    )
  }

  if (!preview) {
    for (const key of Object.keys(files)) files[key] = stripEditAttrs(files[key])
  }

  files['css/styles.css'] = css
  files['js/main.js'] = mainJs
  if (!preview) files['README.md'] = readme(config, pages)

  return { files, pages, zipName: `${slugify(brand.name)}-${pageType}` }
}
